package org.example;

public class 조건문 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        System.out.print("정수를 입력하세요: ");
        int number = sc.nextInt();

        if (number % 2 == 0) {
            System.out.println("짝수입니다");
        }
        else if(number % 2 == 0){
            System.out.println("짝수입니다");
        }


        sc.close();
    }
}
